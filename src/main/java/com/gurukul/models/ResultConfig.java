package com.gurukul.models;

public class ResultConfig {
    private int classId;
    private int theoryMax;
    private int theoryPass;
    private int internalMax;
    private int internalPass;
    private int vivaMax;
    private int vivaPass;
    private int gradeA;
    private int gradeB;
    private int gradeC;
    private int gradeD;

    // Getters and Setters
    public int getClassId() { return classId; }
    public void setClassId(int classId) { this.classId = classId; }

    public int getTheoryMax() { return theoryMax; }
    public void setTheoryMax(int theoryMax) { this.theoryMax = theoryMax; }

    public int getTheoryPass() { return theoryPass; }
    public void setTheoryPass(int theoryPass) { this.theoryPass = theoryPass; }

    public int getInternalMax() { return internalMax; }
    public void setInternalMax(int internalMax) { this.internalMax = internalMax; }

    public int getInternalPass() { return internalPass; }
    public void setInternalPass(int internalPass) { this.internalPass = internalPass; }

    public int getVivaMax() { return vivaMax; }
    public void setVivaMax(int vivaMax) { this.vivaMax = vivaMax; }

    public int getVivaPass() { return vivaPass; }
    public void setVivaPass(int vivaPass) { this.vivaPass = vivaPass; }

    public int getGradeA() { return gradeA; }
    public void setGradeA(int gradeA) { this.gradeA = gradeA; }

    public int getGradeB() { return gradeB; }
    public void setGradeB(int gradeB) { this.gradeB = gradeB; }

    public int getGradeC() { return gradeC; }
    public void setGradeC(int gradeC) { this.gradeC = gradeC; }

    public int getGradeD() { return gradeD; }
    public void setGradeD(int gradeD) { this.gradeD = gradeD; }
}
